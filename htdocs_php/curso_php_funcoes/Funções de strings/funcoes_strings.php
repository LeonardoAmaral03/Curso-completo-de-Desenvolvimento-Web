<?php

	// strtolower
	$texto = "Curso Completo de PHP 10 # %";
	echo strtolower($texto);
	echo '<br /><br />';

	// strtoupper
	$texto = "Curso Completo de PHP 10 # %";
	echo strtoupper($texto);
	echo '<br /><br />';

	// ucfirst -> converte somente a primeira letra da string em maiuscula (primeiro posição da string)
	$texto = "curso completo de php 10 # %";
	echo ucfirst($texto);
	echo '<br /><br />';

?>